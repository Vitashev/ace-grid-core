import { fileURLToPath } from "node:url";
import { mkdirSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const rootDir = resolve(fileURLToPath(new URL("..", import.meta.url)));
const packageName = process.argv[2];
if (!packageName) {
  throw new Error("Usage: node ../../scripts/write-public-package-types.mjs <package-dir-name>");
}

const packageDir = resolve(rootDir, "packages", packageName);
const distDir = join(packageDir, "dist");
mkdirSync(distDir, { recursive: true });

const prelude = "/* Public Ace Grid Core type surface. Generated by export-public-core. */\n";

const coreTypes = `${prelude}import type React from "react";

export type CellValue = string | number | boolean | null | undefined | Date | Record<string, unknown> | unknown[];
export interface GridRow {
  id: string | number;
  data: Record<string, CellValue>;
  [key: string]: unknown;
}
export interface GridColumnDef {
  key: string;
  title?: string;
  header?: string;
  width?: number;
  type?: string;
  [key: string]: unknown;
}
export interface GridProps {
  data?: {
    rows?: GridRow[];
    columns?: GridColumnDef[];
  };
  rows?: GridRow[];
  columns?: GridColumnDef[];
  layout?: {
    width?: number | string;
    height?: number | string;
    rowHeight?: number;
    headerHeight?: number;
    className?: string;
    style?: React.CSSProperties;
  };
  [key: string]: unknown;
}
export type UseGridOptions = Record<string, unknown>;
export type GridActions = Record<string, unknown>;
export declare const Grid: React.FC<GridProps>;
export { Grid as AceGrid };
export declare const GridCell: React.FC<Record<string, unknown>>;
export declare const CellEditor: React.FC<Record<string, unknown>>;
export declare const useGrid: (options: UseGridOptions) => GridActions;
export declare const PaginationBar: React.FC<Record<string, unknown>>;
export declare const GridSearchProvider: React.FC<React.PropsWithChildren<Record<string, unknown>>>;
export declare const GridThemeProvider: React.FC<React.PropsWithChildren<Record<string, unknown>>>;
export declare const PinningEngine: unknown;
export declare const ColumnFilter: React.FC<Record<string, unknown>>;
export declare const RowFilter: unknown;
export declare const GRID_SYSTEM_COLUMN_IDS: readonly string[];
export declare const GRID_SYSTEM_COLUMN_KEYS: Record<string, string>;
export declare const GRID_CAPABILITY_DEFINITIONS: readonly unknown[];
export declare const GRID_CAPABILITY_IDS: readonly string[];
export declare const GRID_TIER_CAPABILITIES: Record<string, readonly string[]>;
export declare const createGridCapabilityError: (...args: unknown[]) => Error;
export declare const isGridCapabilityId: (value: unknown) => boolean;
export declare const resolveGridCapabilities: (...args: unknown[]) => Set<string>;
export declare const availableThemes: Record<string, unknown>;
export declare const dataDarkTheme: unknown;
export declare const dataTheme: unknown;
export declare const defaultTheme: unknown;
export declare const defaultThemeName: string;
export declare const liquidGlassDarkTheme: unknown;
export declare const liquidGlassTheme: unknown;
export declare const material3DarkTheme: unknown;
export declare const material3Theme: unknown;
export declare const resolveTheme: (...args: unknown[]) => unknown;
`;

const wcTypes = `${prelude}import type { GridProps, UseGridOptions, GridActions } from "@ace-grid/core";

export interface AceGridCustomElement<TProps extends Record<string, unknown> = GridProps> extends HTMLElement {
  props: TProps;
  gridProps: TProps;
  setProps: (partial: Partial<TProps>) => void;
  getProps: () => TProps;
  rerender: () => void;
}
export interface AceGridUseGridElement extends AceGridCustomElement {
  hookOptions: UseGridOptions;
  actions: GridActions | null;
  api: GridActions | null;
}
export declare const defineAceGridElement: (tagName?: string) => CustomElementConstructor;
export declare const defineAceGridUseGridElement: (tagName?: string) => CustomElementConstructor;
export declare const createAceGridElementClass: (...args: unknown[]) => CustomElementConstructor;
export declare const createAceGridUseGridElementClass: (...args: unknown[]) => CustomElementConstructor;
`;

const angularTypes = `${prelude}import type { GridProps } from "@ace-grid/core";
export declare const ACE_GRID_ANGULAR_DEFAULT_TAG_NAME = "ace-grid";
export declare class AceGridComponent<TProps extends Record<string, unknown> = Partial<GridProps>> {
  customTagName: string;
  props: TProps;
}
`;

const vueTypes = `${prelude}import type { GridProps } from "@ace-grid/core";
export interface AceGridVueComponentProps<TProps extends Record<string, unknown> = Partial<GridProps>> {
  props?: TProps;
  customTagName?: string;
}
export declare const AceGridVue: unknown;
export default AceGridVue;
`;

const svelteTypes = `${prelude}import type { GridProps } from "@ace-grid/core";
declare const AceGridSvelte: import("svelte").Component<{
  props?: Partial<GridProps>;
  customTagName?: string;
}>;
export default AceGridSvelte;
`;

const filesByPackage = {
  core: {
    "core.d.ts": coreTypes,
  },
  wc: {
    "wc.d.ts": wcTypes,
  },
  angular: {
    "angular.d.ts": angularTypes,
    "angular-core.d.ts": angularTypes,
    "angular-pro.d.ts": angularTypes,
    "angular-enterprise.d.ts": angularTypes,
  },
  vue: {
    "vue.d.ts": vueTypes,
    "vue-core.d.ts": vueTypes,
    "vue-pro.d.ts": vueTypes,
    "vue-enterprise.d.ts": vueTypes,
  },
  svelte: {
    "svelte.d.ts": svelteTypes,
    "svelte-core.d.ts": svelteTypes,
    "svelte-pro.d.ts": svelteTypes,
    "svelte-enterprise.d.ts": svelteTypes,
  },
};

const files = filesByPackage[packageName];
if (!files) {
  throw new Error(`Unknown public package: ${packageName}`);
}

for (const [fileName, content] of Object.entries(files)) {
  writeFileSync(join(distDir, fileName), content);
}
